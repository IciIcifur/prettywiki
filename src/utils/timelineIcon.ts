export default function PickTimeLineIcon(
  titles: string[],
  description: string
) {
  let text = '';
  for (const title of titles) text += title + ' ';
  text = (text + ' ' + description).toLowerCase();

  for (const entry of iconMap) {
    if (entry.keywords.some((word) => text.includes(word))) {
      return entry.icon;
    }
  }
  return 'i-lucide-earth';
}
